function setLanguage(language) {
    document.getElementById('name').innerHTML = translations[language].name;
    document.getElementById('contact').innerHTML = translations[language].contact;
    document.getElementById('about-text').innerHTML = translations[language].aboutText;
    document.getElementById('experience-list').innerHTML = `<li>${translations[language].experienceText}</li>`;
    document.getElementById('education-list').innerHTML = `<li>${translations[language].educationText}</li>`;
    document.getElementById('skills-list').innerHTML = `<li>${translations[language].skillsText}</li>`;
    document.getElementById('projects-list').innerHTML = `<li>${translations[language].projectsText}</li>`;
    document.getElementById('about-title').innerHTML = translations[language].about;
    document.getElementById('experience-title').innerHTML = translations[language].experience;
    document.getElementById('education-title').innerHTML = translations[language].education;
    document.getElementById('skills-title').innerHTML = translations[language].skills;
    document.getElementById('projects-title').innerHTML = translations[language].projects;
    document.getElementById('armath-title').innerHTML = translations[language].armath;
    document.getElementById('sevan-youth-title').innerHTML = translations[language].sevanYouth;
}

function toggleSection(sectionId) {
    const section = document.getElementById(sectionId);
    section.classList.toggle('active');
}

function showLoadingAnimation() {
    const loader = document.createElement('div');
    loader.id = 'loader';
    loader.innerHTML = '<div class="spinner"></div>';
    document.body.appendChild(loader);
    setTimeout(() => {
        loader.remove();
    }, 1000);
}

document.addEventListener('DOMContentLoaded', () => {
    showLoadingAnimation();
    setLanguage('en');
    document.querySelectorAll('.section-toggle').forEach(button => {
        button.addEventListener('click', () => {
            const sectionId = button.getAttribute('data-section');
            toggleSection(sectionId);
        });
    });
    document.querySelectorAll('#language-switcher button').forEach(button => {
        button.addEventListener('click', () => {
            const language = button.getAttribute('data-lang');
            showLoadingAnimation();
            setTimeout(() => {
                setLanguage(language);
            }, 1000);
        });
    });
});